def _load_builder_module():
    spec = importlib.util.spec_from_file_location("xunfang_report_builder", BUILDER)
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    return module


def _effective_paragraph_value(paragraph, name):
    direct = getattr(paragraph.paragraph_format, name)
    if direct is not None:
        return direct
    return getattr(paragraph.style.paragraph_format, name)


def _word_boolean_is_enabled(element) -> bool:
    value = element.get(qn("w:val"))
    return value is None or value.strip().lower() in WORD_TRUE_VALUES


def _package_hygiene_violations(path: Path) -> list[str]:
    violations = set()
    absolute_path = re.compile(
        r"(?:(?<![A-Za-z0-9])[A-Za-z]:[\\/]|/Users/|/home/|/mnt/data/|file:///)",
        re.I,
    )
    secret_value = re.compile(
        r"(?:bearer\s+[A-Za-z0-9._~+/=-]{8,}|"
        r"(?:api[_ -]?key|password|token)\s*[:=]\s*\S+|"
        r"(?:密码|口令|令牌|密钥)\s*[:：=]\s*\S+|"
        r"\b[a-f0-9]{32,}\b)",
        re.I,
    )
    unfinished = re.compile(r"\b(?:TODO|TBD|FIXME|XXX|DRAFT)\b", re.I)
    citation_token = re.compile(
        r":codex-file-citation\{|cite|turn\d+(?:search|open|fetch|view)\d+|【\d+†[^】]*】",
        re.I,
    )
    tool_token = re.compile(r"python-docx|generated by python-docx|libreoffice|openai|chatgpt|normal\.dotm", re.I)

    with ZipFile(path) as archive:
        for name in archive.namelist():
            data = archive.read(name)
            if not (name.endswith(".xml") or name.endswith(".rels")):
                continue
            root = etree.fromstring(data)

            if name == "docProps/app.xml":
                for element in root:
                    local_name = etree.QName(element).localname
                    if local_name in {"Application", "AppVersion", "Template"}:
                        value = (element.text or "").strip()
                        if value:
                            violations.add(f"{name}: non-neutral {local_name}={value!r}")

            if name == "docProps/core.xml":
                for element in root:
                    local_name = etree.QName(element).localname
                    value = (element.text or "").strip()
                    if local_name == "title":
                        if value != "关于“巡防百科”网站建设及推广应用情况的报告":
                            violations.add(f"{name}: unexpected title={value!r}")
                    elif value:
                        violations.add(f"{name}: non-neutral {local_name}={value!r}")

            values = []
            for element in root.iter():
                element_name = etree.QName(element).localname
                element_namespace = etree.QName(element).namespace
                if element_name.lower().startswith("rsid"):
                    violations.add(f"{name}: revision element {element_name}")
                if element_namespace == W14_NS and element_name == "docId":
                    violations.add(f"{name}: w14:docId")
                if element.text:
                    values.append(element.text)
                for attribute_name, attribute_value in element.attrib.items():
                    attribute_local_name = etree.QName(attribute_name).localname
                    if attribute_local_name.lower().startswith("rsid"):
                        violations.add(
                            f"{name}: revision attribute {attribute_local_name}"
                        )
                    values.append(attribute_value)

            for value in values:
                for label, pattern in (
                    ("tool identifier", tool_token),
                    ("internal path", absolute_path),
                    ("secret", secret_value),
                    ("unfinished marker", unfinished),
                    ("tool citation token", citation_token),
                ):
                    match = pattern.search(value)
                    if match:
                        violations.add(f"{name}: {label} {match.group(0)!r}")
    return sorted(violations)


class ReportTests(unittest.TestCase):
    @classmethod
    def setUpClass(cls):
        cls.doc = Document(REPORT)
        cls.text = "\n".join(p.text for p in cls.doc.paragraphs)

    def all_document_paragraphs(self):
        for paragraph in self.doc.paragraphs:
            yield paragraph
        for table in self.doc.tables:
            for row in table.rows:
                for cell in row.cells:
                    yield from cell.paragraphs
        for section in self.doc.sections:
            for container in (section.header, section.footer):
                yield from container.paragraphs
                for table in container.tables:
                    for row in table.rows:
                        for cell in row.cells:
                            yield from cell.paragraphs

    def text_runs(self, paragraph):
        runs = [run for run in paragraph.runs if run.text.strip()]
        self.assertTrue(runs, f"paragraph has no text runs: {paragraph.text!r}")
        return runs

    def assert_font(self, paragraph, names, size, bold=None):
        for run in self.text_runs(paragraph):
            self.assertIn(run.font.name, names)
            self.assertAlmostEqual(run.font.size.pt, size, places=1)
            if bold is not None:
                self.assertEqual(run.bold, bold)


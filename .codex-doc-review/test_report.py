    def test_fresh_build_is_byte_deterministic_and_matches_committed_artifact(self):
        builder = _load_builder_module()
        with tempfile.TemporaryDirectory(prefix="xunfang-report-determinism-") as temp_dir:
            first = Path(temp_dir) / "first.docx"
            second = Path(temp_dir) / "second.docx"
            builder.build_report(first)
            time.sleep(2.2)
            builder.build_report(second)
            first_bytes = first.read_bytes()
            second_bytes = second.read_bytes()
            with ZipFile(first) as archive:
                infos = archive.infolist()
                archive_names = [info.filename for info in infos]
                archive_timestamps = {info.date_time for info in infos}
                archive_metadata_is_stable = all(
                    info.create_system == 0
                    and info.external_attr == FIXED_ZIP_EXTERNAL_ATTR
                    and info.internal_attr == 0
                    and info.extra == b""
                    and info.comment == b""
                    for info in infos
                )
        committed_bytes = REPORT.read_bytes()
        builds_equal = first_bytes == second_bytes
        committed_equal = committed_bytes == first_bytes
        entry_order_is_stable = archive_names == sorted(archive_names)
        timestamps_are_fixed = archive_timestamps == {FIXED_ZIP_TIMESTAMP}
        hashes = {
            "first": hashlib.sha256(first_bytes).hexdigest(),
            "second": hashlib.sha256(second_bytes).hexdigest(),
            "committed": hashlib.sha256(committed_bytes).hexdigest(),
        }
        self.assertTrue(
            builds_equal
            and committed_equal
            and entry_order_is_stable
            and timestamps_are_fixed
            and archive_metadata_is_stable,
            (
                f"builds_equal={builds_equal}, committed_equal={committed_equal}, "
                f"entry_order_is_stable={entry_order_is_stable}, "
                f"timestamps={archive_timestamps}, "
                f"archive_metadata_is_stable={archive_metadata_is_stable}, hashes={hashes}"
            ),
        )

